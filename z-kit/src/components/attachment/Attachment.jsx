// --- HELPERS ---

function getIconConfigFromName(name) {
    if (!name || typeof name !== 'string') {
        return { component: DefaultIcon, className: '' };
    }

    const trimmed = name.trim();
    const lastDot = trimmed.lastIndexOf('.');

    // No extension found (no dot, or dot is the first/last character with nothing after it)
    if (lastDot === -1 || lastDot === trimmed.length - 1) {
        return { component: DefaultIcon, className: '' };
    }

    const format = trimmed.slice(lastDot + 1).toLowerCase();
    return FILE_ICON_MAP[format] || { component: DefaultIcon, className: '' };
}

const getFileExtension = (filename) => {
    if (!filename) return '';
    return filename.split('.').pop()?.toLowerCase() || '';
};

// Accepts either a raw byte count or an already-formatted string ("2.4 MB")
// so existing callers that pre-format sizes keep working unchanged.
const formatBytes = (bytes) => {
    if (typeof bytes !== 'number' || Number.isNaN(bytes)) return bytes;
    if (bytes === 0) return '0 B';
    const units = ['B', 'KB', 'MB', 'GB', 'TB'];
    const exponent = Math.min(Math.floor(Math.log(bytes) / Math.log(1024)), units.length - 1);
    const value = bytes / 1024 ** exponent;
    const precision = exponent === 0 ? 0 : 1;
    return `${value.toFixed(precision)} ${units[exponent]}`;
};

// --- MAIN COMPONENT ---
//
// status:
//   'uploading' — shows an animated progress bar + percentage, cancel button
//   'complete'  — shows the final file size, remove button
//   'error'     — shows the error message, retry button
//
// progress: 0-100, only meaningful while status === 'uploading'.
// If the caller doesn't have real byte-level progress (e.g. the upload
// library only exposes a promise), pass status="uploading" with no
// progress and the bar will render in an indeterminate state instead of
// silently sitting at 0%.

export const Attachment = ({
    name,
    size,
    status = 'uploading',
    progress,
    indeterminate = false,
    errorMessage = 'Upload failed',
    onCancel,
    onRetry,
    onRemove,
}) => {
    const extension = getFileExtension(name);
    const { component: IconComponent, className } = getIconConfigFromName(name);
    const displaySize = formatBytes(size);

    const isUploading = status === 'uploading';
    const isError = status === 'error';
    const hasKnownProgress = !indeterminate && typeof progress === 'number' && !Number.isNaN(progress);
    const clampedProgress = hasKnownProgress ? Math.min(100, Math.max(0, progress)) : null;

    const handleActionClick = () => {
        if (isUploading) {
            onCancel?.();
        } else if (isError) {
            onRetry?.();
        } else {
            onRemove?.();
        }
    };

    let actionIcon = <CloseIcon />;
    let actionLabel = 'Cancel upload';
    if (isError) {
        actionIcon = <RetryIcon />;
        actionLabel = 'Retry upload';
    } else if (status === 'complete') {
        actionIcon = <CloseIcon />;
        actionLabel = 'Remove file';
    }

    let statusText;
    if (isError) {
        statusText = errorMessage;
    } else if (isUploading) {
        statusText = hasKnownProgress ? `${Math.round(clampedProgress)}% \u2022 ${displaySize}` : `Uploading \u2022 ${displaySize}`;
    } else {
        statusText = displaySize;
    }

    return (
        <div className={`attachment${isError ? ' attachment--error' : ''}${isUploading ? ' attachment--uploading' : ''}`}>
            <div className={`file-icon ${className}`} data-ext={extension}>
                <IconComponent />
            </div>

            <div className="attachment-details">
                <div className="file-name">
                    <p>{name}</p>
                </div>

                <div className="file-size">
                    <p className={isError ? 'error-text' : undefined}>{statusText}</p>
                </div>
            </div>

            <Button
                variant="ghost"
                size="small"
                buttonType="icon"
                colorScheme='neutralColor'
                icon={actionIcon}
                aria-label={actionLabel}
                onClick={handleActionClick}
            />

            {isUploading && (
                <div
                    className={`loading-progress-track ${hasKnownProgress ? '' : 'loading-progress-track--indeterminate'}`}
                    role="progressbar"
                    aria-label={`Uploading ${name}`}
                    aria-valuenow={hasKnownProgress ? Math.round(clampedProgress) : undefined}
                    aria-valuemin={0}
                    aria-valuemax={100}
                >
                    {hasKnownProgress && (
                        <div
                            className="loading-progress-fill"
                            style={{ width: `${clampedProgress}%` }}
                        />
                    )}
                </div>
            )}
        </div>
    );
};